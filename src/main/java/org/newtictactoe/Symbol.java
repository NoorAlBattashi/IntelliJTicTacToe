package org.newtictactoe;

public class Symbol {
    private String symbol;

    /**
     * This method used to set the value of the symbol
     *
     * @param s
     */
    public Symbol(String s) {
        symbol = s;
    }

    /**
     * This method used to set the value of the symbol
     *
     * @return symbol
     */
    public String getSymbol() {
        return symbol;
    }

}
